//generic memory latency measurement function
//called by specific functions
static uint64_t memory_access(uint64_t offset)
{
    uint64_t start, end;
    volatile uint8_t *ptr = 0;
    volatile uint64_t *v;
    uint64_t cr3;
    
    uint64_t voffset =
        vspace_map_attr(mem->frame_cap, 0, mem->bytes,
                        PTABLE_ACCESS_DEFAULT | PTABLE_CACHE_DISABLED, NULL, NULL);
    
    // map the page uncacheable
    if(paging_map_device(offset, (size_t) BASE_PAGE_SIZE) != 0) {
        return (-1);
    }

    // Setup stride buffer
    char *buf = (char *)voffset;
    for (int i = 0; i < BUFSIZE / SIZE_CACHELINE; i++)
        *((uint64_t *)&buf[i * SIZE_CACHELINE]) =
            (uint64_t)&buf[(rand_stride[i] * SIZE_CACHELINE)];

    // flush all TLB entries to be fair
    __asm__ __volatile__("mov %%cr3,%0" : "=a" (cr3) : );
    __asm__ __volatile__("mov %0,%%cr3" :  : "a" (cr3));

    //touch the page to load the TLB, store value in a volatile
    //variable to ensure that it is not removed by the optimizer
    v = (uint64_t *)*(uint64_t *)(ptr + voffset);
    
    // Unroll the loop a bit to mitigate tight-loop noise
    start = rdtscp();
    for (int i = 0; i < MICROBENCH_ITERATIONS; i += 64) {
        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;
        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;
        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;
        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;

        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;
        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;
        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;
        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;

        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;
        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;
        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;
        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;

        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;
        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;
        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;
        v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v; v = (uint64_t *)*v;
    }
    end = rdtscp();

    // Assign to a volatile, so the above is not optimized away
    ptr = (volatile uint8_t *)v;

    //store result and compute "overflow", if needed
    if (start <= end) {
        return end - start;
    } else {
        return 0xffffffffffffffff - start + 1 + end;
    }
}

#define MEM_SWEEP_MIN   (100 * 1024 * 1024)
#define MEM_SWEEP_MAX   (16UL * 1024 * 1024 * 1024)
#define MEM_SWEEP_STEP  (100 * 1024 * 1024)

void memory_sweep(void)
{
    printf("Sweeping memory...\n");

    for(uint64_t i = MEM_SWEEP_MIN; i < MEM_SWEEP_MAX; i += MEM_SWEEP_STEP) {
        // Skip memory hole between 3G and 4G
        if(i >= 3UL * 1024 * 1024 * 1024 && i < 4UL * 1024 * 1024 * 1024) {
            continue;
        }

        uint64_t res = memory_access(mb, i);
        printf("Sweep at %lu has latency %lu cycles.\n", i, res);
    }
}
